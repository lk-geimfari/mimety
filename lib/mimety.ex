defmodule Mimety do
  @moduledoc """
  Documentation for Mimety.
  Mimety is a tiny library for Elixir that help you handle mime types.
  More here: https://www.iana.org/assignments/media-types/media-types.xhtml
  """

  @doc """
    iex> Mimety.extension("cpp")
    "text/x-c"

    iex(1)> Mimety.mime_to_extension("text/x-c")
    ["c", "cc", "cxx", "cpp", "h", "hh", "dic"]
  """

  @doc """
  Get mime type of file by extension.
  """
  @spec extension(String.t) :: String.t
  def extension(ext) do
    extensions(ext)
  end

  @doc """
  Get file extension by mime type.
  """
  @spec mime_to_extension(String.t) :: String.t
  def mime_to_extension(mime) do
    mimetypes(mime)
  end


